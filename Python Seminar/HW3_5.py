# 5 -3 2 -1 1 0 1 1 2 3 5


n = int(input('Введите длину ряда: '))
f1 = f2 = 1

f_list = [-1, 1, 0, 1, 1]

for i in range(2, n):
    f1, f2 = f2, f1 + f2
    f_list.append(f2)
    f_list.insert(0, f2*(1-i % 2*2))

fib = " ".join(map(str, f_list))
print(fib)
